import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";

function argValue(name) {
  const index = process.argv.indexOf(name);
  return index >= 0 ? process.argv[index + 1] : "";
}

function httpOrigin(value) {
  const text = String(value || "").trim().replace(/\/+$/, "");
  if (!text) return "";

  const url = new URL(text.includes("://") ? text : `https://${text}`);
  if (url.protocol !== "http:" && url.protocol !== "https:") {
    throw new Error(`Unsupported server origin protocol: ${url.protocol}`);
  }
  return url.origin;
}

function webSocketUrlFromOrigin(origin) {
  const url = new URL(origin);
  url.protocol = url.protocol === "https:" ? "wss:" : "ws:";
  url.pathname = "/ws";
  url.search = "";
  url.hash = "";
  return url.toString();
}

function extractRuntimeConfigFromHtml(html) {
  const match = String(html || "").match(
    /<script\b[^>]*>\s*window\.__QXP_RUNTIME__\s*=\s*(\{[\s\S]*?\})\s*;?\s*<\/script>/m
  );
  if (!match) {
    throw new Error("Could not find window.__QXP_RUNTIME__ in the production HTML.");
  }
  return JSON.parse(match[1]);
}

const configUrl = argValue("--url") || process.env.QXP_RUNTIME_CONFIG_URL;
const outputPath = resolve(argValue("--out") || "dist/runtime-config.js");
const explicitServerOrigin = httpOrigin(argValue("--server-origin") || process.env.QXP_SERVER_ORIGIN);

let outputScript;
let configSource;
let rtcStatus = "unknown";

if (configUrl) {
  const response = await fetch(configUrl);
  if (!response.ok) {
    throw new Error(`Could not fetch ${configUrl}: HTTP ${response.status}`);
  }

  const runtimeConfig = extractRuntimeConfigFromHtml(await response.text());
  const serverOrigin = explicitServerOrigin || httpOrigin(runtimeConfig.apiBaseUrl || runtimeConfig.serverOrigin);
  const payload = {
    ...runtimeConfig,
    ...(serverOrigin
      ? {
          serverOrigin,
          apiBaseUrl: runtimeConfig.apiBaseUrl || serverOrigin,
          wsUrl: runtimeConfig.wsUrl || webSocketUrlFromOrigin(serverOrigin)
        }
      : {})
  };

  outputScript = `// Generated by scripts/sync-runtime-config.mjs. Do not edit by hand.\nwindow.__QXP_RUNTIME__ = ${JSON.stringify(payload)};\n`;
  configSource = configUrl;
  rtcStatus = payload.rtc?.callsEnabled ? "enabled" : "disabled";
} else {
  const localRuntimePath = resolve("public/runtime-config.js");
  const localRuntime = await readFile(localRuntimePath, "utf8");

  if (explicitServerOrigin) {
    const payload = {
      serverOrigin: explicitServerOrigin,
      apiBaseUrl: explicitServerOrigin,
      wsUrl: webSocketUrlFromOrigin(explicitServerOrigin)
    };
    outputScript = `// Generated by scripts/sync-runtime-config.mjs. Do not edit by hand.\nwindow.__QXP_RUNTIME__ = ${JSON.stringify(payload)};\n`;
  } else {
    outputScript = localRuntime;
  }

  configSource = localRuntimePath;
}

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, outputScript, "utf8");

console.log(`Wrote ${outputPath} from ${configSource} with RTC ${rtcStatus}.`);
